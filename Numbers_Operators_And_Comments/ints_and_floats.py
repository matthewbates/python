
#* INTEGERS AND FLOATS
# integer
    # can be positive or negative
    # ex: 4, 57, -10
# floating point
    # can also be positive or negative, but have decimals
    # ex: 6.1, 1.3333, 0.0

# if the expression contains a float, the end result will be a float
print(1 + 1.0) #2.0

#* NUMBERS AND MATH
# + (addition), - (subtraction), * (multiplication), / (division), ** (exponentiation), % (modulo), // (integer division - if you don't want a float)

# DIVISION ALWAYS RETURNS A FLOAT
print(1/2) #0.5
print(6/2) #3.0

# integer division does NOT return a float
print(10//3) #3
print(8//3) #2  